package study_0727;

public class class01 {
	public static void main(String[] args) { 		// 프론트엔드
		int num = 123;
		double f = 123.1578;
		char c = 'a';
		String str = "\"Hello\"";
		boolean b = true;
		
		
		System.out.print(num + "\n"); 					// java에서 쓰는 출력함수, ln - line new
		System.out.print(f + "\n");
		System.out.print(c + "\n");
		System.out.print(str + "\n");
		System.out.print(b + "\n");
		System.out.print("\n");
		System.out.println();
	}
}

