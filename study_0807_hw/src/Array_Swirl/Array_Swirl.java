package Array_Swirl;

public class Array_Swirl {
	public static void main(String[] args) {

//		1  2  3  4  5  6  7  8 
//		28 29 30 31 32 33 34 9 
//		27 48 49 50 51 52 35 10 
//		26 47 60 61 62 53 36 11 
//		25 46 59 64 63 54 37 12 
//		24 45 58 57 56 55 38 13 
//		23 44 43 42 41 40 39 14 
//		22 21 20 19 18 17 16 15 

//		1  2  3  4  5  6  7  8		
//		28                   9 
//		27                   10 
//		26                   11 
//		25                   12 
//		24                   13 
//		23                   14 
//		22 21 20 19 18 17 16 15 
//		[0][0] ~ [0][7] / [1][7] ~ [7][7] / [7][6] ~ [7][0] / [6][0] ~ [1][0]
		
//		   29 30 31 32 33 34  
//		   48             35
//		   47             36
//		   46             37
//		   45             38
//		   44 43 42 41 40 39
//		[1][1] ~ [1][6] / [2][6] ~ [6][6] / [6][5] ~ [6][1] / [5][1] ~ [2][1]
		
//		      49 50 51 52
//		      60       53
//		      59       54
//		      58 57 56 55
//		[2][2] ~ [2][5] / [3][5] ~ [5][5] / [5][4] ~ [5][2] / [4][2] ~ [3][2]
		
//		         61 62
//		         64 63		
//		[3][3] ~ [3][4] / [4][4] / [4][3] / X

		int[][] arr = new int[8][8];
		int num = 1;

		for (int i = 0; i < 8; i++) {

			// [0][0] ~ [0][7], [1][1] ~ [1][6], [2][2] ~ [2][5], [3][3] ~ [3][4]
			// ▷ 오른쪽으로 가는 경우
			for (int j = i; j < 8 - i; j++) {
				arr[i][j] = num++;
			}

			// [1][7] ~ [7][7], [2][6] ~ [6][6], [3][5] ~ [5][5], [4][4]
			// ▽ 아래로 가는 경우
			for (int j = i + 1; j < 8 - i; j++) {
				arr[j][8 - i - 1] = num++;
			}

			// [7][6] ~ [7][0], [6][5] ~ [6][1], [5][4] ~ [5][2], [4][3]
			// ◁ 왼쪽으로 가는 경우
			for (int j = 8 - i - 2; j >= i; j--) {
				arr[8 - i - 1][j] = num++;
			}

			// [6][0] ~ [1][0], [5][1] ~ [2][1], [4][2] ~ [3][2], x
			// △ 위로 가는 경우
			for (int j = 8 - i - 2; j > i; j--) {
				arr[j][i] = num++;
			}
		}

		for (int i = 0; i < 8; i++) {
			for (int j = 0; j < 8; j++) {
				if (arr[i][j] < 10) {
					System.out.print(" " + arr[i][j] + " ");
				} else {
					System.out.print(arr[i][j] + " ");
				}
			}
			System.out.println();
		}

	}
}

